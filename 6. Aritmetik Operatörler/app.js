/*

ARİTMETİK OPERATÖRLER

= operatörü eşittir anlamına gelmez çünkü bu bir atama operatörüdür
+ operatörü matematikteki gibi sayıları toplarken, string tiplerini birleştirir ör: number + number toplanırken, string + number birleştirilir
- operatörü matematikteki çıkarma işlemi
* operatörü
/ operatörü
% operatörü bir sayıyı başka bir sayıya böldüğümüzde kalanı bulmak için kullanılır
++ operatörü bir arttırmak için kullanılır
-- operatörü bir eksiltmek için kullanılır
** operatörü bir sayının üssünü almak için kullanılır


*/

//! ""="" Operatörü
let a = 5;
let isim = "Dilara";
console.log(a);
console.log(isim);

//! "+" Operatörü
let not1 = 60;
let not2 = 70;
console.log(not1+not2);

//! "-" Operatörü
let sayi1 = 100;
let sayi2 = 50;
console.log(sayi1 - sayi2);

//! "*" Operatörü
let num1 = 5;
let num2 = 3;
console.log(num1*num2);

//! "/" Operatörü
let m = 100;
let n = 80;
let sonuc = (m + n)/2;
console.log(sonuc)

//! "%" Operatörü
console.log(12%5);

//! "++" Operatörü
let s = 10; // s=10
s++; // s=11
s++; // s=12
s++; // s=13;
console.log(s);

//! "--" Operatörü
let k = 10; // k=10
k--; // k=11
k--; // k=12
k--; // k=13;
console.log(k);

//! "**" Operatörü
console.log(2**3);
console.log(5**3);